/**
 * Injects build metadata into apps/desktop/src/lib/build-metadata/generated.ts
 * Reads version from apps/desktop/package.json, git commit SHA and date, RELEASE_CHANNEL env var.
 * Plain CommonJS — runs with `node` directly, no TypeScript compilation needed.
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const repoRoot = path.resolve(__dirname, '..');
const desktopPkg = JSON.parse(
  fs.readFileSync(path.join(repoRoot, 'apps/desktop/package.json'), 'utf-8')
);

const version = desktopPkg.version || '0.0.0';

let commitSha = 'unknown';
let commitDate = 'unknown';

try {
  commitSha = execSync('git rev-parse --short HEAD', { cwd: repoRoot, encoding: 'utf-8' }).trim();
  commitDate = execSync('git log -1 --format=%ci', { cwd: repoRoot, encoding: 'utf-8' }).trim();
} catch (e) {
  console.warn('[inject-build-metadata] Could not read git info:', e.message);
}

const releaseChannel = process.env.RELEASE_CHANNEL || 'dev';

const generated = `// AUTO-GENERATED — DO NOT EDIT MANUALLY
// Generated by scripts/inject-build-metadata.js at build time
export const BUILD_METADATA = {
  version: '${version}',
  commitSha: '${commitSha}',
  commitDate: '${commitDate}',
  releaseChannel: '${releaseChannel}',
} as const;
`;

const outputPath = path.join(repoRoot, 'apps/desktop/src/lib/build-metadata/generated.ts');
fs.writeFileSync(outputPath, generated, 'utf-8');
console.log('[inject-build-metadata] Written to', outputPath);
console.log('  version:', version);
console.log('  commitSha:', commitSha);
console.log('  commitDate:', commitDate);
console.log('  releaseChannel:', releaseChannel);
